/******************************************************************************/
/**
 * @brief   
 * @param   
 * @return  
 * @note    
*******************************************************************************/
void CVWOLEDimage::update_error(OLED_ERROR i_error_e, bool i_b_err_flag)
{
	u32 target_err_bit_val = 0x00;	 
	target_err_bit_val = ((m_oled_info_t.n_error_num >> i_error_e) & 0x01);

	//update   
	if( target_err_bit_val != i_b_err_flag )
	{
		m_oled_info_t.b_error_update_flag = true;
		if( i_b_err_flag == true )
		{
			m_oled_info_t.n_error_num |= (0x01 << i_error_e);
		}
		else
		{
			m_oled_info_t.n_error_num &= ~(0x01 << i_error_e);
		}
	}


}

