{
	u8 cur_seq_e;
	cur_seq_e = (u8)get_cur_basic_display_seq();
	cur_seq_e++;
	
	/* Error    , Error  skip */
	if(m_oled_info_t.n_error_num == 0 && cur_seq_e == eOLED_ERROR)
		cur_seq_e++;
		
	if(cur_seq_e == (u8)eOLED_LAST)
		cur_seq_e = (u8)eOLED_OFF;
		
	set_basic_display_seq((OLED_DISP_BASIC_SEQ)cur_seq_e);

	print_log(INFO, 1, "[%s] OLED basic display seq : %d.\n", m_p_log_id, cur_seq_e);
}

	
