/******************************************************************************/
/**
* \brief        
* \param        none
* \return       none
* \note
*******************************************************************************/
void CVWOLEDimage::oled_proc(void)
{
    print_log( DEBUG, 1, "[%s] start oled proc...\n", m_p_log_id );
    
    OLED_STATE	oled_state_e = eOLED_IDLE_STATE;

    state_print(m_oled_state_e);
    
    while( m_b_oled_proc_is_running )
    {
        if( oled_state_e != m_oled_state_e )
        {
            state_print(oled_state_e);
            m_oled_state_e = oled_state_e;
            m_p_spi_c->oled_clear();
           	
           	if(m_oled_state_e == eOLED_IDLE_STATE)
           	{
           		m_p_spi_c->oled_clear();
           		set_basic_display_seq(eOLED_OFF);
				oled_display_onoff(false);
			}
        }
        
        switch( (u8)oled_state_e )
        {
            case eOLED_IDLE_STATE:
            {
                oled_state_e = state_oled_idle();
                break;
            }
            case eOLED_BASIC_DISP_STATE:
            {
				oled_init();		//ESD OLED     (HW       ,    . delay  0.3  )
                oled_state_e = state_oled_basic_disp();
                break;
            }
            case eOLED_ACTION_DISP_STATE:
            {
				//20220524. Youngjun Han.     ,  ,  OLED   oled init  . 
				// ,  , OLED    ,   .
				if( (m_cur_oled_event != eACQ_IMAGE_EVENT) &&
				    (m_cur_oled_event != eSAVE_IMAGE_EVENT) &&
					(m_cur_oled_event != eSEND_IMAGE_EVENT) )
				{
					oled_init();		//  OLED   (ES5_59)    (HW       ,    . delay  0.3  )
				}
                oled_state_e = state_oled_action_disp();
                break;
            }
            default:
            {
                break;
            }
        }
        
    } /* end of while( m_b_oled_proc_is_running ) */
}

