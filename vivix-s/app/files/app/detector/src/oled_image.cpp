/**
 * @brief
 * @param
 * @return
 * @note
*******************************************************************************/
void* oled_routine( void * arg )
{
	CVWOLEDimage *oled_c = (CVWOLEDimage *)arg;
	oled_c->oled_proc();
	pthread_exit( NULL );
}


/********************************************************************************
 * \brief		get_mode_img
 * \param
 * \return
 * \note
 ********************************************************************************/
u8* CVWOLEDimage::get_mode_img(u32 i_n_mode, u32 i_n_status)
{
	if( i_n_mode == 0 ) // tether
	{
		/* i_n_status [0: 10/100M speed, 1: 1G speed] */
		return (u8*)m_p_mode_tether_img[i_n_status];	
	}
	else if( i_n_mode == 1 ) //station
	{
		/* i_n_status [link quality] */
		if(m_wifi_step_num_e == eWIFI_SIGNAL_STEP_3)
			return (u8*)m_p_mode_wifi_img[i_n_status];
		else
			return (u8*)m_p_mode_station_img[i_n_status];
	}
	else if( i_n_mode == 2 ) //ap 
	{
		return (u8*)m_p_mode_ap_img;
	}
	else
	{
		return (u8*)m_p_mode_none_img;
	}
}

