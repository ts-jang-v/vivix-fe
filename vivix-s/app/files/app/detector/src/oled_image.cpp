/********************************************************************************
 * \brief		
 * \param
 * \return
 * \note
 ********************************************************************************/
void CVWOLEDimage::get_error_img(u8* o_p_img, u32* o_p_len)
{
	char p_str[9];
	u32 n_idx = 0;
	
	m_oled_info_t.b_error_update_flag = false;

	if(m_oled_info_t.n_error_num == 0)
	{
		*o_p_len = 0;
		return;
	}
	else
	{
		for( n_idx = 0; n_idx < eOLED_ERROR_MAX; n_idx++ )
		{
			if(m_oled_info_t.n_error_num & (0x01 << n_idx))
				break;
		}
	}	
		
	sprintf(p_str, " ERR_%02d ", n_idx);
	make_string_img_data(o_p_img, (u8*)p_str, OLED_ERROR_INFO_LENGTH);
	*o_p_len = OLED_ERROR_INFO_LENGTH;
	
}

/********************************************************************************
 * \brief		
 * \param
 * \return
 * \note
 ********************************************************************************/
void CVWOLEDimage::get_operating_img(u8* o_p_img, u32* o_p_len)
{
	char p_str[OLED_OPER_INFO_MAX_LENGTH];
	u32 n_len = 0;
	
	OLED_EVENT cur_event_e = m_cur_oled_event;
	
	if(m_cur_oled_event <= eACTION_EVENT_MIN || m_cur_oled_event >= eACTION_EVENT_MAX)
	{
		*o_p_len = 0;
		return;
	}
	
	memset(p_str, 0, OLED_OPER_INFO_MAX_LENGTH);

	if(cur_event_e == eBOOTING)		// Wake up
	{
		strcpy(p_str, "Start...");
	}
	else if(cur_event_e == eWAKEUP_EVENT)		// Wake up
	{
		strcpy(p_str, "Wake up...");	
	}
	else if(cur_event_e == eRESET_WLAN_EVENT)		// initialize wirless setting
	{
		strcpy(p_str, "Reset WLAN...");
	}
	else if(cur_event_e == eSYNC_WLAN_EVENT)		// Synchronize wirless setting
	{	
		strcpy(p_str, "Sync WLAN...");
	}
	else if(cur_event_e == eACQ_IMAGE_EVENT)		// Debug mode, Acquire an image
	{	
		strcpy(p_str, "Acq image...");
	}
	else if(cur_event_e == eSAVE_IMAGE_EVENT)		// Debug mode, Save an image
	{
		strcpy(p_str, "Save image...");
	}
	else if(cur_event_e == eSEND_IMAGE_EVENT)		// Debug mode, Send an image
	{
		strcpy(p_str, "Send image...");
	}
	else if(cur_event_e == eAP_MODE_ENABLE_EVENT)
	{
		strcpy(p_str, "AP Mode Start...");
	}
	else if(cur_event_e == eSTA_MODE_ENABLE_EVENT)	
	{
		strcpy(p_str, "STA Mode Start...");
	}
	else if(cur_event_e == eSCAN_AP_EVENT)		
	{
		strcpy(p_str, "AP scan...");
	}
	else if(cur_event_e == eCHANGE_AP_EVENT)		
	{
		strcpy(p_str, "Change AP ...");
	}
	
	n_len = strlen(p_str);
	n_len =  n_len > OLED_OPER_INFO_MAX_LENGTH ? OLED_OPER_INFO_MAX_LENGTH:n_len;	
	
	make_string_img_data(o_p_img, (u8*)p_str, n_len);

	*o_p_len = n_len;
}




/********************************************************************************
 * \brief		
 * \param
 * \return
 * \note
 ********************************************************************************/
void CVWOLEDimage::make_string_img_data(u8* o_p_dest_buf, u8* i_p_src_str, u32 i_n_len)
{
	u8 c_char;
	u8* p_src;	
	u32 i;
	
	for( i = 0; i < i_n_len; i++)
	{
		c_char = i_p_src_str[i];
		if( c_char >= 'a' && c_char <= 'z')
		{
			p_src = (u8*)m_p_lower_char[c_char - 'a'];
		}
		else if (c_char >= 'A' && c_char <= 'Z')
		{
			p_src = (u8*)m_p_upper_char[c_char - 'A'];
		}
		else if( c_char >= '0' && c_char <= '9')
		{
			p_src = (u8*)m_p_numeric[c_char - '0'];
		}
		else
		{
			if( c_char == '-')
			{
				p_src = (u8*)m_p_hyphen;
			}
			else if( c_char == '_')
			{
				p_src = (u8*)m_p_underbar;
			}
			else if( c_char == '.')
			{
				p_src = (u8*)m_p_point;
			}
			else if( c_char == ':')
			{
				p_src = (u8*)m_p_colon;
			}
			else if( c_char == ',')
			{
				p_src = (u8*)m_p_comma;
			}
			else if( c_char == ' ')
			{
				p_src = (u8*)m_p_blank;
			}
			else
			{
				p_src = (u8*)m_p_blank;
			}			
		}
		
		memcpy(&o_p_dest_buf[OLED_IMG_CHARACTER_SIZE_BYTE*i], p_src, OLED_IMG_CHARACTER_SIZE_BYTE);
	}	
}	
		
