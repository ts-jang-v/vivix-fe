/********************************************************************************
 * \brief		get_scroll_img
 * \param
 * \return
 * \note
 ********************************************************************************/
void CVWOLEDimage::get_scroll_img(u8* o_p_img, u32* o_p_total_colum_num)
{
	u32			o_n_len = 0;
	
	if(get_cur_state() == eOLED_BASIC_DISP_STATE)
	{
		OLED_DISP_BASIC_SEQ cur_seq_e = get_cur_basic_display_seq();
		if(cur_seq_e == eOLED_IP)
		{
			get_ip_img(o_p_img, &o_n_len);
		}
		else if(cur_seq_e == eOLED_SSID)	
		{
			get_ssid_img(o_p_img, &o_n_len);
		}	
		else if(cur_seq_e == eOLED_ERROR)	
		{
			// Error  , scroll   
			get_error_img(o_p_img, &o_n_len);
			*o_p_total_colum_num = o_n_len*OLED_IMG_CHARACTER_WIDTH/8;
			return;
		}		
		else
		{
			*o_p_total_colum_num = 0;
			return;
		}
	}
	else if(get_cur_state() == eOLED_ACTION_DISP_STATE)
	{
		get_operating_img(o_p_img, &o_n_len);
	}
	else
	{
		*o_p_total_colum_num = 0;
		return;
	}
			
	// scroll 
	o_n_len += 5;
	*o_p_total_colum_num = o_n_len*OLED_IMG_CHARACTER_WIDTH/8;
}

