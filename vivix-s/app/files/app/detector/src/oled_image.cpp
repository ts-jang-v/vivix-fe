/******************************************************************************/
/**
 * @brief   
 * @param   
 * @return  
 * @note    
*******************************************************************************/
void CVWOLEDimage::draw_main_display(bool i_b_draw_all) 
{
	u8* p_img;
	
	if(i_b_draw_all == true)
	{
		// Battery 
		oled_set_region_by_area(eOLED_BAT);
		p_img = get_battery_img();
		m_p_spi_c->oled_display(p_img,oled_get_size_of_area(eOLED_BAT));
	}
		
	// Battery  		
	oled_set_region_by_area(eOLED_BAT_SOC);	
	p_img = get_bat_soc_img(m_oled_info_t.main_t.w_battery_gauge);
	m_p_spi_c->oled_display(p_img,oled_get_size_of_area(eOLED_BAT_SOC));
	
	//  
	oled_set_region_by_area(eOLED_CHARGE_EN);	
	p_img = get_charge_bat_img(m_oled_info_t.main_t.b_charge);
	m_p_spi_c->oled_display(p_img,oled_get_size_of_area(eOLED_CHARGE_EN));
	
	// Preset 
	u32 o_len = 0;
	memset(g_p_scroll_img, 0, OLED_SCROLL_BUFFER_SIZE_MAX);
	get_preset_img(g_p_scroll_img, &o_len);
	oled_set_region_by_area(eOLED_PRESET);	
	m_p_spi_c->oled_display(g_p_scroll_img,oled_get_size_of_area(eOLED_PRESET));
	
	// Network mode sleep  
	oled_set_region_by_area(eOLED_MODE);	
	if(m_oled_info_t.main_t.w_power_mode != ePM_MODE_NORMAL)
	{
		p_img = get_sleep_img();
	}
	else
	{
		if(m_oled_info_t.main_t.w_network_mode == eNET_MODE_TETHER)
		{
			p_img = get_mode_img(m_oled_info_t.main_t.w_network_mode, m_oled_info_t.main_t.c_1G_speed);
		}
		else if(m_oled_info_t.main_t.w_network_mode == eNET_MODE_STATION)
		{
			p_img = get_mode_img(m_oled_info_t.main_t.w_network_mode, m_oled_info_t.main_t.w_link_quality_level);
		}
		else
		{
			p_img = get_mode_img(m_oled_info_t.main_t.w_network_mode, 0);
		}
	}
	m_p_spi_c->oled_display(p_img,oled_get_size_of_area(eOLED_MODE));	
}

