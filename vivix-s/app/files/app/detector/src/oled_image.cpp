/******************************************************************************/
/**
* \brief        
* \param        none
* \return       none
* \note
*******************************************************************************/
void CVWOLEDimage::set_oled_display_event(OLED_EVENT i_event_e)
{
	lock();
		
	if(i_event_e == eBUTTON_EVENT)
	{
		if(get_cur_state() == eOLED_ACTION_DISP_STATE)
		{
			print_log(INFO, 1, "[%s] Operation info state, ignore button event.\n", m_p_log_id);
			unlock();
			return;
		}
			
		m_b_button_event_req = true;
	}
	else if(i_event_e > eACTION_EVENT_MIN && i_event_e < eACTION_EVENT_MAX)
	{	
		//   , MAIN   display  
		if(i_event_e == eBOOTING)	
			m_b_return_to_basic_disp = true;
		
		m_b_action_event_req = true;
	}
	else if(i_event_e == eACTION_EVENT_END)
	{
		if(get_cur_state() != eOLED_ACTION_DISP_STATE && m_b_action_event_req == false)
		{	
			print_log(INFO, 1, "[%s] Invalid operation end event.\n", m_p_log_id);
			unlock();
			return;
		}		
	}
	else if(i_event_e == eEVENT_OFF)
	{
		print_log(INFO, 1, "[%s] OLED off by detector power-off.\n", m_p_log_id);
	}
	else
	{
		print_log(INFO, 1, "[%s] Invalid oled event : %d.\n", m_p_log_id, i_event_e);
		unlock();
		return;
	}		
	
	m_cur_oled_event = i_event_e;
	print_log(INFO, 1, "[%s] set_oled_display_event : %d.\n", m_p_log_id, i_event_e);
	
	unlock();
}
	


/******************************************************************************/
/**
* \brief        
* \param        none
* \return       none
* \note
*******************************************************************************/
OLED_STATE CVWOLEDimage::state_oled_idle(void)
{
	if(m_b_action_event_req == true)
	{
		m_b_action_event_req = false;
		return eOLED_ACTION_DISP_STATE;
	}
	
	if(m_b_button_event_req == true)
	{
		m_b_button_event_req = false;
		return eOLED_BASIC_DISP_STATE;
	}	
	
	sleep_ex(100000);		// 100msec
	
	return eOLED_IDLE_STATE;		
}

/******************************************************************************/
/**
* \brief        
* \param        none
* \return       none
* \note
*******************************************************************************/
OLED_STATE CVWOLEDimage::state_oled_basic_disp(void)
{
	OLED_STATE ret_e = eOLED_BASIC_DISP_STATE;
	u32 n_idx;
	
	bool b_scroll = true;
	
	oled_display_onoff(true);
	
	set_next_basic_display_seq();
	if(get_cur_basic_display_seq() == eOLED_OFF)
	{
		return eOLED_IDLE_STATE;	
	}
	
	if(get_cur_basic_display_seq() == eOLED_MAIN)
	{	
		memcpy(&m_oled_info_t.main_t, &m_updated_main_t, sizeof(main_info_t));
		draw_main_display(true);	
		b_scroll = false;
	}
	else
	{
		b_scroll = prepare_scroll_img();
	}

	//state_oled_basic_disp  ,   display  ,  while loop   1 display .
	//1  scroll   scroll .
	//ERROR  , 1 display , ERROR    ,      .
	n_idx = 0;
	while(1)
	{
		if(is_oled_off_condition() == true || m_b_action_event_req == true)
		{
			m_b_return_to_basic_disp = m_b_action_event_req?true:false;
			ret_e =  eOLED_IDLE_STATE;	
			break;		
		}
		
		if(m_b_button_event_req == true)
		{
			m_b_button_event_req = false;
			ret_e =  eOLED_BASIC_DISP_STATE;
			break;	
		}
		
		if(get_cur_basic_display_seq() == eOLED_ERROR && m_oled_info_t.b_error_update_flag)
		{
			print_log(INFO, 1, "[%s] error_update_flag set. update OLED error status.\n", m_p_log_id);
			
			b_scroll = prepare_scroll_img();
		}
		else if(get_cur_basic_display_seq() == eOLED_MAIN)
		{
			if( memcmp(&m_oled_info_t.main_t, &m_updated_main_t, sizeof(main_info_t)) != 0 )
			{
				memcpy(&m_oled_info_t.main_t, &m_updated_main_t, sizeof(main_info_t));
				draw_main_display(false);
			}

			if(m_b_preset_blink == true)
			{
				blink_preset_name();
			}
		}
			
		if(b_scroll == true)
		{
			scroll_img(&n_idx);
			n_idx++;
		}
		else
		{
			sleep_ex(500000);
		}
		
	}

	return ret_e;
}

/******************************************************************************/
/**
* \brief        
* \param        none
* \return       none
* \note
*******************************************************************************/
OLED_STATE CVWOLEDimage::state_oled_action_disp(void)
{
	u32 n_idx;
	OLED_STATE ret_e = eOLED_BASIC_DISP_STATE;
	
	bool b_scroll = true;
		
	oled_display_onoff(true);
	
	b_scroll = prepare_scroll_img();

	n_idx = 0;	
	while(1)
	{
		if(m_cur_oled_event == eACTION_EVENT_END)
		{
			// Basic       ,    MAIN  
			if(m_b_return_to_basic_disp == true)
			{
				print_log(INFO, 1, "[%s] Return to basic display.\n", m_p_log_id);
				m_b_return_to_basic_disp = false;
				ret_e = eOLED_BASIC_DISP_STATE;
			}
			else
			{
				ret_e =  eOLED_IDLE_STATE;
			}
			
			break;		
		}
		
		if(m_b_action_event_req == true || is_oled_off_condition() == true)
		{
			ret_e =  eOLED_IDLE_STATE;
			break;		
		}
						
		if(b_scroll == true)
		{
			scroll_img(&n_idx);
			n_idx++;
		}
		else
		{
			sleep_ex(100000);
		}
		
	}

	return ret_e;
}	


/********************************************************************************
 * \brief		
 * \param
 * \return
 * \note
 ********************************************************************************/
bool CVWOLEDimage::prepare_scroll_img(void)
{
	u32 n_end_column_num = 0;
	
	oled_set_region_by_area(eOLED_INFO);
	memset(g_p_scroll_img, 0, OLED_SCROLL_BUFFER_SIZE_MAX);
	get_scroll_img(g_p_scroll_img, &n_end_column_num);
	
	m_n_scroll_end_column = n_end_column_num;

	m_p_spi_c->oled_display(g_p_scroll_img, oled_get_size_of_area(eOLED_INFO));		
	
	if(n_end_column_num <= SCROLL_START_COLUMN_IDX)
	{
		return false;
	}
	
	return true;
}


/********************************************************************************
 * \brief		
 * \param
 * \return
 * \note
 ********************************************************************************/
void CVWOLEDimage::scroll_img(u32* i_p_idx)
{
	u8* p_img;
	u32 n_idx = *i_p_idx;
	
	u32 n_cur_column_idx = SCROLL_START_COLUMN_IDX + n_idx;

	if(n_cur_column_idx > m_n_scroll_end_column)
	{
		p_img = g_p_scroll_img;
		n_cur_column_idx = SCROLL_START_COLUMN_IDX;
		*i_p_idx = 0;
	}
	else
	{
		p_img = g_p_scroll_img + (OLED_IMG_INFO_HEIGHT * n_idx);
	}
		
		
	m_p_spi_c->oled_display(p_img,oled_get_size_of_area(eOLED_INFO));	

	// scroll     500msec ,  200msec  8pixel  scroll
	if(n_cur_column_idx == SCROLL_START_COLUMN_IDX)
	{
		sleep_ex(500000);
	}
	else
	{
		sleep_ex(200000);
	}		
}
	
	
	
