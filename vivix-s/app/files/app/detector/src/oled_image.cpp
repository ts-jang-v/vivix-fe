/********************************************************************************
 * \brief		
 * \param
 * \return
 * \note
 ********************************************************************************/
void CVWOLEDimage::start(void)
{	
	m_p_spi_c = NULL;
	m_p_spi_c = new CVWSPI(m_n_gpio_fd, m_n_spi_fd, print_log);
	
	oled_init();
	
    m_b_oled_proc_is_running = true; 
	if( pthread_create(&m_oled_thread_t, NULL, oled_routine, ( void * ) this)!= 0 )
    {
    	m_b_oled_proc_is_running = false;
    	print_log( ERROR, 1, "[%s] oled_routine pthread_create:%s\n", m_p_log_id, strerror( errno ));
    }
}

